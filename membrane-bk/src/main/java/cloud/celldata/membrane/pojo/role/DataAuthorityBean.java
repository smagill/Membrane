/**
 * 数据权限实体
 *
 */
public class DataAuthorityBean {

    // 数据权限key
    private Integer key;

    // 数据权限名称
    private String label;

    public Integer getKey() {
        return key;
    }

    public void setKey(Integer key) {
        this.key = key;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
